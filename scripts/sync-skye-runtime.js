#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import ts from "typescript";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const root = path.resolve(__dirname, "..");

const sourcePath = path.join(root, "src", "lib", "skye", "skyeSecure.ts");
const outputPath = path.join(root, "public", "_shared", "skye", "skyeSecure.js");

if (!fs.existsSync(sourcePath)) {
  console.error(`[sync-skye-runtime] Missing source file: ${path.relative(root, sourcePath)}`);
  process.exit(1);
}

const source = fs.readFileSync(sourcePath, "utf8");
const result = ts.transpileModule(source, {
  compilerOptions: {
    target: ts.ScriptTarget.ES2022,
    module: ts.ModuleKind.ES2022,
    lib: ["es2022", "dom", "dom.iterable"],
    removeComments: false,
  },
  fileName: sourcePath,
  reportDiagnostics: true,
});

const errors = (result.diagnostics || []).filter((diag) => diag.category === ts.DiagnosticCategory.Error);
if (errors.length) {
  for (const diagnostic of errors) {
    const message = ts.flattenDiagnosticMessageText(diagnostic.messageText, "\n");
    console.error(`[sync-skye-runtime] ${message}`);
  }
  process.exit(1);
}

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
const banner = `// Generated by scripts/sync-skye-runtime.js from src/lib/skye/skyeSecure.ts\n`;
fs.writeFileSync(outputPath, `${banner}${result.outputText}`, "utf8");
console.log(`[sync-skye-runtime] Wrote ${path.relative(root, outputPath)}`);
